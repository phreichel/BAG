#ifndef __scanner_hpp__
#define __scanner_hpp__

#include <istream>

//-------------------------------------------------------------------------------
using namespace std;
//-------------------------------------------------------------------------------

//-------------------------------------------------------------------------------
class Scanner {

    public:

        //-----------------------------------------------------------------------
        Scanner(istream& ins);
        ~Scanner();
        //-----------------------------------------------------------------------

        //-----------------------------------------------------------------------
        char look() const;
        int  symbol() const;
        bool next();
        int  status() const;
        //-----------------------------------------------------------------------

        //-----------------------------------------------------------------------
        bool isMatch(char c);
        bool isLower() const;
        bool isUpper() const;
        bool isAlpha() const;
        bool isBinary() const;
        bool isOctal() const;
        bool isDecimal() const;
        bool isHexadecimal() const;
        bool isWhitespace() const;
        bool isNewline() const;
        //-----------------------------------------------------------------------

    private:

        //-----------------------------------------------------------------------
        char _look;
        int  _symbol;
        int  _status;
        istream* _ins;
        //-----------------------------------------------------------------------

};
//-------------------------------------------------------------------------------

#endif

