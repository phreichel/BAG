//*************************************************************************************************
package jlib.platform;
//*************************************************************************************************

import com.jogamp.newt.event.KeyEvent;
import com.jogamp.newt.event.KeyListener;
import com.jogamp.newt.event.MouseEvent;
import com.jogamp.newt.event.MouseListener;

import jlib.application.Application;
import jlib.input.InputData;
import jlib.message.Message;

//*************************************************************************************************
class InputEventHandler implements KeyListener, MouseListener {

	//=============================================================================================
	private Application application;
	//=============================================================================================

	//=============================================================================================
	public InputEventHandler(Application app) {
		application = app;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void mouseClicked(MouseEvent e) {
	}
	//=============================================================================================

	//=============================================================================================
	public void mouseEntered(MouseEvent e) {
	}
	//=============================================================================================

	//=============================================================================================
	public void mouseExited(MouseEvent e) {
	}
	//=============================================================================================

	//=============================================================================================
	public void mousePressed(MouseEvent e) {
	}
	//=============================================================================================

	//=============================================================================================
	public void mouseReleased(MouseEvent e) {
	}
	//=============================================================================================

	//=============================================================================================
	public void mouseMoved(MouseEvent e) {
		var data = new InputData(InputData.EVENT_MOVED, e.getX(), e.getY());
		var message = new Message(Message.CH_INPUT, data); 
		application.post(message);
	}
	//=============================================================================================

	//=============================================================================================
	public void mouseDragged(MouseEvent e) {
		var data = new InputData(InputData.EVENT_MOVED, e.getX(), e.getY());
		var message = new Message(Message.CH_INPUT, data); 
		application.post(message);
	}
	//=============================================================================================

	//=============================================================================================
	public void mouseWheelMoved(MouseEvent e) {
	}
	//=============================================================================================

	//=============================================================================================
	public void keyPressed(KeyEvent e) {
	}
	//=============================================================================================

	//=============================================================================================
	public void keyReleased(KeyEvent e) {
	}
	//=============================================================================================

}
//*************************************************************************************************
