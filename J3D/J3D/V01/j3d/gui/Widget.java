//*************************************************************************************************
package j3d.gui;
//*************************************************************************************************

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;

//*************************************************************************************************
public class Widget {


	//=============================================================================================
	private WidgetType type;
	private Widget parent;
	private final List<Widget> children = new ArrayList<>(10);
	private final List<Widget> children_ro = Collections.unmodifiableList(children);
	//=============================================================================================

	//=============================================================================================
	public WidgetType type() {
		return type;
	}
	//=============================================================================================

	//=============================================================================================
	public void type(WidgetType type) {
		this.type = type;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Widget parent() {
		return parent;
	}
	//=============================================================================================
	
	//=============================================================================================
	public List<Widget> children() {
		return children_ro;
	}
	//=============================================================================================

	//=============================================================================================
	public void attach(Widget child) {
		attach(children.size(), child);
	}
	//=============================================================================================
	
	//=============================================================================================
	public void attach(int idx, Widget child) {
		child.parent = this;
		children.add(idx, child);
	}
	//=============================================================================================

	//=============================================================================================
	public void detach() {
		parent.children.remove(this);
		parent = null;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
