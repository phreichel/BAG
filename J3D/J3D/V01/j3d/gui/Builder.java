//*************************************************************************************************
package j3d.gui;
//*************************************************************************************************

//*************************************************************************************************
public class Builder {

	//=============================================================================================
	@SuppressWarnings("unused")
	private final GUI gui;
	//=============================================================================================

	//=============================================================================================
	public Builder(GUI gui) {
		this.gui = gui;
	}
	//=============================================================================================

	//=============================================================================================
	public Widget widget() {
		return new Widget();
	}
	//=============================================================================================
	
	//=============================================================================================
	public Widget buildRoot() {
		Widget widget = widget();
		widget.type(WidgetType.ROOT);
		return widget; 
	}
	//=============================================================================================
	
}
//*************************************************************************************************
