//*************************************************************************************************
package pge.util;
//*************************************************************************************************

//*************************************************************************************************
public final class XPGE extends RuntimeException {

	//=============================================================================================
	private static final long serialVersionUID = 1L;
	//=============================================================================================

	//=============================================================================================
	public final XPGEType type;
	//=============================================================================================

	//=============================================================================================
	public XPGE() {
		this(XPGEType.NONE);
	}
	//=============================================================================================

	//=============================================================================================
	public XPGE(Throwable cause) {
		this(XPGEType.NONE, cause);
	}
	//=============================================================================================

	//=============================================================================================
	public XPGE(String message) {
		this(XPGEType.NONE, message);
	}
	//=============================================================================================
	
	//=============================================================================================
	public XPGE(String message, Throwable cause) {
		this(XPGEType.NONE, message, cause);
	}
	//=============================================================================================

	//=============================================================================================
	public XPGE(String message, Object ... params) {
		this(XPGEType.NONE, message, params);
	}
	//=============================================================================================
	
	//=============================================================================================
	public XPGE(XPGEType type) {
		super();
		this.type = type;
	}
	//=============================================================================================

	//=============================================================================================
	public XPGE(XPGEType type, Throwable cause) {
		super(cause);
		this.type = type;
	}
	//=============================================================================================

	//=============================================================================================
	public XPGE(XPGEType type, String message) {
		super(message);
		this.type = type;
	}
	//=============================================================================================

	//=============================================================================================
	public XPGE(XPGEType type, String message, Throwable cause) {
		super(message, cause);
		this.type = type;
	}
	//=============================================================================================

	//=============================================================================================
	public XPGE(XPGEType type, String message, Object ... params) {
		super(String.format(message, params));
		this.type = type;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
