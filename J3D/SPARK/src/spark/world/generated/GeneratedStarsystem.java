//*********************************************************************************************************************
package spark.world.generated;
//*********************************************************************************************************************

import javax.vecmath.Vector3f;

import spark.world.Galaxy;
import spark.world.Starsystem;
import spark.world.Universe;

//*********************************************************************************************************************
public class GeneratedStarsystem extends Generated<GeneratedStarsystem> implements Starsystem {

	//=================================================================================================================
	private GeneratedGalaxy galaxy;
	private Vector3f location;
	//=================================================================================================================
	
	//=================================================================================================================
	public GeneratedStarsystem(long seed, Vector3f location, GeneratedGalaxy galaxy) {
		super(seed);
		this.location = location;
		this.galaxy = galaxy;
	}
	//=================================================================================================================

	//=================================================================================================================
	public Universe universe() {
		return this.galaxy().universe();
	}
	//=================================================================================================================

	//=================================================================================================================
	public Galaxy galaxy() {
		return this.galaxy;
	}
	//=================================================================================================================
	
	//=================================================================================================================
	public Vector3f location() {
		return this.location;
	}
	//=================================================================================================================

}
//*********************************************************************************************************************
