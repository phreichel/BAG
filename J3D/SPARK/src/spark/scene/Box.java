//*********************************************************************************************************************
package spark.scene;
//*********************************************************************************************************************

import javax.vecmath.Vector3f;
import spark.platform.Graphics;

//*********************************************************************************************************************
public class Box {

	//=================================================================================================================
	public Vector3f center = new Vector3f();
	public Vector3f extent = new Vector3f(1, 1, 1);
	//=================================================================================================================
	
	//=================================================================================================================
	public Box(Vector3f location) {
		center.set(location);
	}
	//=================================================================================================================

	//=================================================================================================================
	public void render(Graphics g) {
		g.draw3DPoint(center.x, center.y, center.z);
	}
	//=================================================================================================================
	
}
//*********************************************************************************************************************
