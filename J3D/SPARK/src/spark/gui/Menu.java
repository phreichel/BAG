//*********************************************************************************************************************
package spark.gui;
//*********************************************************************************************************************

//*********************************************************************************************************************
public class Menu extends Component {

	//=================================================================================================================
	public Menu() {
		
		inputHandler(InputHandler::GROUP);
		renderer(Renderer::GROUP);
		
		Layout layout = Layout::VERTICAL;
		property(Property.LAYOUT, layout);
		
		var container = new Container();
		container.component(this);
		property(Property.CHILDREN, container);
		
	}
	//=================================================================================================================

	//=================================================================================================================
	public void add(Component component) {
		var container = (Container) property(Property.CHILDREN);
		container.item(component);
	}
	//=================================================================================================================
}
//*********************************************************************************************************************
