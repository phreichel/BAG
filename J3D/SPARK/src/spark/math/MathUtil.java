package spark.math;
//*********************************************************************************************************************

import javax.vecmath.Matrix4f;
import javax.vecmath.Vector3f;

//*********************************************************************************************************************
public class MathUtil {

	//=================================================================================================================
	public static float[] copyMatrixToArray(float[] dst, Matrix4f src) {

		dst[0] = src.m00;
		dst[1] = src.m10;
		dst[2] = src.m20;
		dst[3] = src.m30;

		dst[4] = src.m01;
		dst[5] = src.m11;
		dst[6] = src.m21;
		dst[7] = src.m31;

		dst[8] = src.m02;
		dst[9] = src.m12;
		dst[10] = src.m22;
		dst[11] = src.m32;

		dst[12] = src.m03;
		dst[13] = src.m13;
		dst[14] = src.m23;
		dst[15] = src.m33;
        
        return dst;
	}
	//=================================================================================================================

	//=================================================================================================================
	public static Vector3f sphericalToCartesian(Vector3f dst, float r, float azimut, float elevation) {
		dst.set(
			(float) (r * Math.cos(elevation) * Math.cos(azimut)),
			(float) (r * Math.cos(elevation) * Math.sin(azimut)),
			(float) (r * Math.sin(elevation)));
		return dst;
	}
	//=================================================================================================================
	
}
