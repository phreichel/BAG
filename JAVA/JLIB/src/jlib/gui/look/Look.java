//*************************************************************************************************
package jlib.gui.look;
//*************************************************************************************************

import jlib.gui.Element;
import jlib.platform.Graphics;

//*************************************************************************************************
public interface Look {

	//=============================================================================================
	public void render(Graphics g, Element e);
	//=============================================================================================

	
	//=============================================================================================
	public static final Look DEFAULT = Look::renderDefault;
	public static final Look MARGIN  = Look::renderMargin;
	public static final Look PADDING = Look::renderPadding;
	//=============================================================================================
	
	//=============================================================================================
	private static void renderDefault(Graphics g, Element e) {
		
		float w = e.width();
		float h = e.height();
		
		g.lineColor(.5f, 0f, 0f);
		g.fillColor(.5f, 0f, 0f, .2f);
		
		g.boxFilled(0, 0, w, h);
		g.box(0,  0,  w,  h);
		
	}
	//=============================================================================================

	//=============================================================================================
	private static void renderMargin(Graphics g, Element e) {}
	//=============================================================================================

	//=============================================================================================
	private static void renderPadding(Graphics g, Element e) {

		float w = e.width();
		float h = e.height();
		g.fillColor(0f, 0f, 1f, .2f);
		g.boxFilled(0, 0, w, h);
		
	}
	//=============================================================================================
	
}
//*************************************************************************************************
