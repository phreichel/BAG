//*************************************************************************************************
package jlib.gui.layout;
//*************************************************************************************************

import jlib.gui.Element;

//*************************************************************************************************
public class VerticalScale implements Layout {

	//=============================================================================================
	private float gap = 0f;
	//=============================================================================================

	//=============================================================================================
	public float gap() {
		return gap;
	}
	//=============================================================================================

	//=============================================================================================
	public void gap(float gap) {
		this.gap = gap;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void apply(Element element) {
		
		int n = element.elements();
		if (n==0) return;
		
		float space = (n-1) * gap;
		float w = element.width();
		float h = (element.height() - space) / n;
		
		for (int i=0; i<n; i++) {

			var child = element.element(i);
			
			float x = 0f;
			float y = (h+gap) * i;

			child.position(x, y);
			child.size(w, h);
			
		}
		
	}
	//=============================================================================================

}
//*************************************************************************************************
