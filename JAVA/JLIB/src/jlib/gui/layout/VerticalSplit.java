//*************************************************************************************************
package jlib.gui.layout;
//*************************************************************************************************

import jlib.gui.Element;

//*************************************************************************************************
public class VerticalSplit implements Layout {

	//=============================================================================================
	private float gap;
	private float span;
	private Direction direction = Direction.REGULAR;
	//=============================================================================================

	//=============================================================================================
	public float gap() {
		return gap;
	}
	//=============================================================================================

	//=============================================================================================
	public void gap(float gap) {
		this.gap = gap;
	}
	//=============================================================================================

	//=============================================================================================
	public float span() {
		return span;
	}
	//=============================================================================================

	//=============================================================================================
	public void span(float span) {
		this.span = span;
	}
	//=============================================================================================

	//=============================================================================================
	public Direction direction() {
		return direction;
	}
	//=============================================================================================

	//=============================================================================================
	public void direction(Direction direction) {
		this.direction = direction;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void apply(Element element) {
		switch (direction) {
			case REVERSE: applyReverse(element); break;
			default: applyRegular(element); break;
		}
	}
	//=============================================================================================

	//=============================================================================================
	private void applyRegular(Element element) {

		var a = element.element(0);
		var b = element.element(1);
		
		if (a != null) {
			a.position(0, 0);
			a.size(element.width(), span);
		}

		if (b != null) {
			b.position(0, span+gap);
			b.size(element.width(), element.height() - (span+gap));
		}
		
	}
	//=============================================================================================
	
	//=============================================================================================
	private void applyReverse(Element element) {

		var a = element.element(0);
		var b = element.element(1);
		
		if (a != null) {
			a.position(0, 0);
			a.size(element.width(), element.height() - (span+gap));
		}

		if (b != null) {
			b.position(0, element.height() - span);
			b.size(element.width(), span);
		}
		
	}
	//=============================================================================================

}
//*************************************************************************************************
