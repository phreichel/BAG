//*************************************************************************************************
package jlib.message;
//*************************************************************************************************

//*************************************************************************************************
public class Message {

	//=============================================================================================
	public static final int CH_QUIT  = 0;
	public static final int CH_INPUT = 1;
	//=============================================================================================
	
	//=============================================================================================
	public int    channel;
	public Object content;
	//=============================================================================================

	//=============================================================================================
	public Message(int ch) {
		channel = ch;
		content = null;
	}
	//=============================================================================================

	//=============================================================================================
	public Message(int ch, Object data) {
		channel = ch;
		content = data;
	}
	//=============================================================================================

	//=============================================================================================
	public <C> C content(Class<C> cls) {
		return cls.cast(content);
	}
	//=============================================================================================
	
}
//*************************************************************************************************
