//*************************************************************************************************
package mbot.ui;
//*************************************************************************************************

import mbot.app.AbstractService;
import mbot.app.Application;

//*************************************************************************************************
public class UserInterface extends AbstractService {

	//=============================================================================================
	public Application app = null;
	public MainFrame mainFrame = null;
	//=============================================================================================
	
	//=============================================================================================
	public void init() {
		if (mainFrame != null) {
			mainFrame.setVisible(true);
		}
	}
	//=============================================================================================

	//=============================================================================================
	public void update() {
		if (mainFrame != null) {
			if (!mainFrame.isVisible()) {
				app.terminated = true;
			}
		}
	}
	//=============================================================================================

	//=============================================================================================
	public void done() {
		if (mainFrame != null) {
			mainFrame.dispose();
		}
	}
	//=============================================================================================
	
}
//*************************************************************************************************
