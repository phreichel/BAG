//**************************************************************************************************
package joda.widget;
//**************************************************************************************************

import java.util.List;
import java.util.Set;

//**************************************************************************************************
public interface Widget {

	//==============================================================================================
	public Set<WidgetFlag> getFlags();
	public void addFlag(WidgetFlag flag);
	public void deleteFlag(WidgetFlag flag);
	//==============================================================================================
	
	//==============================================================================================
	public float getX();
	public float getY();
	public void setPosition(float x, float y);
	public void movePosition(float dx, float dy);
	//==============================================================================================

	//==============================================================================================
	public float getWidth();
	public float getHeight();
	public void setSize(float w, float h);
	public void changeSize(float dw, float dh);
	//==============================================================================================

	//==============================================================================================
	public Widget getParent();
	public void setParent(Widget parent);
	//==============================================================================================

	//==============================================================================================
	public List<Widget> getChildren();
	public void addChild(Widget child);
	public void deleteChild(Widget child);
	//==============================================================================================
	
}
//**************************************************************************************************
