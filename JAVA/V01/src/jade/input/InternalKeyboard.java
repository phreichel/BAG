package jade.input;
//**************************************************************************************************

import java.util.Collections;
import java.util.EnumSet;
import java.util.Set;

import lombok.Getter;

//**************************************************************************************************
public class InternalKeyboard implements Keyboard {

	//==============================================================================================
	private Set<Key> keys = EnumSet.noneOf(Key.class); 
	private Set<Key> readonlyKeys = Collections.unmodifiableSet(keys);
	@Getter private boolean capsLock = false;
	@Getter private boolean numLock = false;
	@Getter private boolean scrollLock = false;
	//==============================================================================================

	//==============================================================================================
	public Set<Key> getKeys() {
		return readonlyKeys;
	}
	//==============================================================================================

	//==============================================================================================
	public void setKey(Key key, boolean set) {
		if (set)
			keys.add(key);
		else
			keys.remove(key);
	}
	//==============================================================================================
	
}
