//**************************************************************************************************
package jade.scene;
//**************************************************************************************************

import javax.vecmath.Color3f;

import jade.render.Renderer;
import lombok.Getter;
import lombok.Setter;

//**************************************************************************************************
public class Sphere extends Node {

	//==============================================================================================
	@Getter @Setter private float radius = 1f;
	@Getter private Color3f color = new Color3f(.7f, .7f, .7f); 
	//==============================================================================================

	//==============================================================================================
	public void setColor(Color3f color) {
		this.color.set(color);
	}
	//==============================================================================================

	//==============================================================================================
	public void setColor(float r, float g, float b) {
		this.color.set(r, g, b);
	}
	//==============================================================================================
	
	//==============================================================================================
	protected void renderNodePre(Renderer renderer) {
		renderer.setColor(color.x, color.y, color.z);
		renderer.drawSphere(radius);
	}
	//==============================================================================================
	
}
//**************************************************************************************************
