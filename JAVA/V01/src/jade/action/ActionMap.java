//**************************************************************************************************
package jade.action;
//**************************************************************************************************

import java.util.Map;
import java.util.TreeMap;

//**************************************************************************************************
public class ActionMap {

	//==============================================================================================
	private Map<String, NamedAction> map = new TreeMap<>();
	//==============================================================================================

	//==============================================================================================
	public NamedAction get(String name) {
		return map.get(name);		
	}
	//==============================================================================================
	
	//==============================================================================================
	public void add(NamedAction action) {
		map.put(action.getName(), action);
	}
	//==============================================================================================

	//==============================================================================================
	public ActionMap add(String name, boolean enabled, Action action) {
		NamedAction namedAction = new NamedAction(name, enabled, action);
		add(namedAction);
		return this;
	}
	//==============================================================================================
	
	//==============================================================================================
	public void remove(String name) {
		map.remove(name);
	}
	//==============================================================================================
	
	//==============================================================================================
	public void remove(NamedAction action) {
		remove(action.getName());
	}
	//==============================================================================================
	
}
//**************************************************************************************************
