//**************************************************************************************************
package jade.action;
//**************************************************************************************************

import jade.input.Input;
import jade.input.Trigger;
import lombok.Getter;
import lombok.Setter;

//**************************************************************************************************
public class NamedAction implements Action {

	//==============================================================================================
	@Getter private String name = "";
	@Getter @Setter private boolean enabled = true;
	@Getter @Setter private Action action = null;
	//==============================================================================================

	//==============================================================================================
	public NamedAction() {}
	//==============================================================================================

	//==============================================================================================
	public NamedAction(String name, boolean enabled, Action action) {
		this.name = name;
		this.enabled = enabled;
		this.action = action;
	}
	//==============================================================================================
	
	//==============================================================================================
	public void perform(Trigger trigger, Input input) {
		if (enabled && action != null) {
			action.perform(trigger, input);
		}
	}
	//==============================================================================================
	
}
//**************************************************************************************************
