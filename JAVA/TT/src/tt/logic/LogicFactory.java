//*****************************************************************************
package tt.logic;
//*****************************************************************************

import tt.db.DataFactory;

//*****************************************************************************
public class LogicFactory {

	//=========================================================================
	private DataFactory dataFactory;
	//=========================================================================
	
	//=========================================================================
	public LogicFactory(DataFactory dataFactory) {
		this.dataFactory = dataFactory;
	}
	//=========================================================================
	
	//=========================================================================
	public Nothing createNothing() {
		var session = dataFactory.getSession();
		return new Nothing(session);
	}
	//=========================================================================

}
