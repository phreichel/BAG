package rbr.net;
//*************************************************************************************************

import java.io.IOException;
import java.net.DatagramPacket;
import java.net.DatagramSocket;
import java.net.SocketException;

//*************************************************************************************************
public class NetConnector {

	//=============================================================================================
	private DatagramSocket socket = null;
	//=============================================================================================
	
	//=============================================================================================
	public void init() {
		try { 
			socket = new DatagramSocket(1234, null);
		} catch (SocketException e) {
			e.printStackTrace();
		}
	}
	//=============================================================================================

	//=============================================================================================
	public void send() {
		try { 
			byte[] payload = new byte[1024];
			var packet = new DatagramPacket(null, payload.length);
			socket.send(packet);
		} catch (IOException e) {
			e.printStackTrace();
		}
	}
	//=============================================================================================
	
	//=============================================================================================
	public void done() {
		if (socket != null) {
			socket.close();
			socket = null;
		}
	}
	//=============================================================================================
	
}
//*************************************************************************************************
