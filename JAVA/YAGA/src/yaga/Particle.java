//*****************************************************************************
package yaga;
//*****************************************************************************

import javax.vecmath.Vector3f;

//*****************************************************************************
public class Particle {

	//=========================================================================
	public Vector3f loc = new Vector3f();
	public Vector3f vel = new Vector3f();
	public int      gen = 0;
	//=========================================================================

	//=========================================================================
	public Particle spawn(Particle p) {
		p.loc.set(loc);
		p.vel.set(vel);
		p.gen = gen+1;
		return p;
	}
	//=========================================================================
	
}
//*****************************************************************************
