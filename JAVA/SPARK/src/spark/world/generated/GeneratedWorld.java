//*********************************************************************************************************************
package spark.world.generated;
//*********************************************************************************************************************

import spark.world.Universe;
import spark.world.World;
import spark.world.generator.WorldGenerator;

//*********************************************************************************************************************
public class GeneratedWorld extends Generated<GeneratedWorld> implements World {

	//=================================================================================================================
	private GeneratedUniverse universe = null;
	//=================================================================================================================

	//=================================================================================================================
	private int spawnGalaxy = -1;
	private int spawnStarsystem = -1; 
	//=================================================================================================================

	//=================================================================================================================
	public GeneratedWorld(long seed) {
		super(seed);
	}
	//=================================================================================================================

	//=================================================================================================================
	public Universe universe() {
		return universe;
	}
	//=================================================================================================================

	//=================================================================================================================
	public void universe(GeneratedUniverse universe) {
		this.universe = universe;
	}
	//=================================================================================================================

	//=================================================================================================================
	public int spawnGalaxy() {
		return spawnGalaxy;
	}
	//=================================================================================================================

	//=================================================================================================================
	public void spawnGalaxy(int index) {
		spawnGalaxy = index;
	}
	//=================================================================================================================
	
	//=================================================================================================================
	public int spawnStarsystem() {
		return spawnStarsystem;
	}
	//=================================================================================================================

	//=================================================================================================================
	public void spawnStarsystem(int index) {
		spawnStarsystem = index;
	}
	//=================================================================================================================
	
	//=================================================================================================================
	public void generate() {		
		WorldGenerator.INSTANCE.generate(this);
		super.generate();
	}
	//=================================================================================================================
	
}
//*********************************************************************************************************************
