package spark.world.generated;
//*********************************************************************************************************************

//*********************************************************************************************************************
public abstract class Generated<G extends Generated<?>> {

	//=================================================================================================================
	private long seed;
	private boolean generated;
	private String name = null;
	private String generatedName = null;
	//=================================================================================================================

	//=================================================================================================================
	public Generated(long seed) {
		this.seed = seed;
		this.generated = false;
		this.generatedName = "" + seed;
	}
	//=================================================================================================================
	
	//=================================================================================================================
	public long seed() {
		return this.seed;
	}
	//=================================================================================================================

	//=================================================================================================================
	public boolean generated() {
		return this.generated;
	}
	//=================================================================================================================
	
	//=================================================================================================================
	public String name() {
		return name != null ? this.name : this.generatedName;
	}
	//=================================================================================================================

	//=================================================================================================================
	public void name(String name) {
		this.name = name;
	}
	//=================================================================================================================
	
	//=================================================================================================================
	public String generatedName() {
		return this.generatedName;
	}
	//=================================================================================================================

	//=================================================================================================================
	public void generatedName(String generatedName) {
		this.generatedName = generatedName;
	}
	//=================================================================================================================
	
	//=================================================================================================================
	public void generate() {
		this.generated = true;
	}
	//=================================================================================================================
	
}
//*********************************************************************************************************************
