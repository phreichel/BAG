//*********************************************************************************************************************
package spark.world.generated;
//*********************************************************************************************************************

import java.util.List;

import javax.vecmath.Vector3f;

import org.eclipse.collections.impl.list.mutable.FastList;

import spark.world.Galaxy;
import spark.world.Starsystem;
import spark.world.Universe;
import spark.world.generator.GalaxyGenerator;

//*********************************************************************************************************************
public class GeneratedGalaxy extends Generated<GeneratedGalaxy> implements Galaxy {

	//=================================================================================================================
	private GeneratedUniverse universe;
	private Vector3f location;
	//=================================================================================================================
	
	//=================================================================================================================
	private List<GeneratedStarsystem> systems = null;
	//=================================================================================================================
	
	//=================================================================================================================
	public GeneratedGalaxy(long seed, Vector3f location, GeneratedUniverse universe) {
		super(seed);
		this.location = location;
		this.universe = universe;
		this.systems = new FastList<GeneratedStarsystem>();
	}
	//=================================================================================================================

	//=================================================================================================================
	public Universe universe() {
		return this.universe;
	}
	//=================================================================================================================

	//=================================================================================================================
	public Vector3f location() {
		return this.location;
	}
	//=================================================================================================================

	//=================================================================================================================
	public int systemCount() {
		return this.systems.size();
	}
	//=================================================================================================================

	//=================================================================================================================
	public void system(GeneratedStarsystem system) {
		this.systems.add(system);
	}
	//=================================================================================================================
	
	//=================================================================================================================
	public Starsystem system(int index) {
		return this.systems.get(index);
	}
	//=================================================================================================================
	
	//=================================================================================================================
	public void generate() {		
		GalaxyGenerator.INSTANCE.generate(this);
		super.generate();
	}
	//=================================================================================================================
	
}
//*********************************************************************************************************************
