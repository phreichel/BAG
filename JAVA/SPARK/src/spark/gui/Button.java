//*********************************************************************************************************************
package spark.gui;
//*********************************************************************************************************************

//*********************************************************************************************************************
public class Button extends Component {

	//=================================================================================================================
	public Button() {
		renderer(Renderer::BUTTON);
		Layout layout = Layout::BUTTON;
		property(Property.LAYOUT, layout);
		text("Button");
	}
	//=================================================================================================================

	//=================================================================================================================
	public Button(String text, Action action) {
		renderer(Renderer::BUTTON);
		Layout layout = Layout::BUTTON;
		property(Property.LAYOUT, layout);
		text(text);
		action(action);
	}
	//=================================================================================================================
	
	//=================================================================================================================
	public String text() {
		return (String) property(Property.TEXT);
	}
	//=================================================================================================================

	//=================================================================================================================
	public void text(String text) {
		property(Property.TEXT, text);
	}
	//=================================================================================================================

	//=================================================================================================================
	public Action action() {
		return (Action) property(Property.TRIGGER);
	}
	//=================================================================================================================

	//=================================================================================================================
	public void action(Action action) {
		property(Property.TRIGGER, action);
	}
	//=================================================================================================================
	
}
//*********************************************************************************************************************
