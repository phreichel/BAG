//*********************************************************************************************************************
package spark.gui;
//*********************************************************************************************************************

import spark.platform.Input;

//*********************************************************************************************************************
public final class InputContext {

	//=================================================================================================================
	public Input input;
	public Component component = null;
	public float xOffset = 0f;
	public float yOffset = 0f;
	public boolean topmost = false;
	//=================================================================================================================

	//=================================================================================================================
	public void enter(Component c) {
		component = c;
		xOffset += c.x();
		yOffset += c.y();
	}
	//=================================================================================================================

	//=================================================================================================================
	public void exit(Component c) {
		if (c.container() == null) {
			component = null;
		} else {
			component = c
				.container()
				.component();
		}
		xOffset -= c.x();
		yOffset -= c.y();
	}
	//=================================================================================================================
	
}
//*********************************************************************************************************************
