//*********************************************************************************************************************
package spark.scene;
//*********************************************************************************************************************

import javax.vecmath.Matrix3f;
import javax.vecmath.Vector3f;
import spark.platform.Graphics;

//*********************************************************************************************************************
public class Camera {

	//=================================================================================================================
	public Vector3f location = new Vector3f(0, 0, 0);
	public Matrix3f orientation = new Matrix3f();
	//=================================================================================================================

	//=================================================================================================================
	public Camera() {
		orientation.setIdentity();
	}
	//=================================================================================================================
	
	//=================================================================================================================
	public void render(Graphics g) {
		g.place3DCamera(
			location,
			orientation);
	}
	//=================================================================================================================
	
}
//*********************************************************************************************************************
