//*************************************************************************************************
package phi.mv.msg;
//*************************************************************************************************

import phi.mv.data.MVConfiguration;

//*************************************************************************************************
public class MVMsgManager {

	//=============================================================================================
	public static MVBaseMsg msg = null;
	//=============================================================================================
	
	//=============================================================================================
	public static void connect() {
		
		if (msg != null) disconnect();
		
		MVConfiguration cfg = MVConfiguration.data;
		
		String host = cfg.wifi.ipAddress;
		int    port = cfg.wifi.ipPort;
		MVBaseMsg wifiMsg = new MVBaseMsg();
		wifiMsg.open(host, port);
		msg = wifiMsg;
		
	}
	//=============================================================================================

	//=============================================================================================
	public static void disconnect() {
		
		if (msg != null) {
			msg.disconnect();
			msg.close();
			msg = null;
		}
		
	}
	//=============================================================================================
	
}
//*************************************************************************************************
