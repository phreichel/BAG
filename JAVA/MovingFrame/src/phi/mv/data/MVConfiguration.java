package phi.mv.data;
//*************************************************************************************************

import java.io.File;

import com.fasterxml.jackson.databind.ObjectMapper;

//*************************************************************************************************
public class MVConfiguration {

	//=============================================================================================
	private static final File FILE = new File("./MVConfig.json");
	//=============================================================================================

	//=============================================================================================
	private static ObjectMapper objectMapper = new ObjectMapper();
	//=============================================================================================

	//=============================================================================================
	public static MVConfiguration data;
	//=============================================================================================
	
	//=============================================================================================
	public static boolean load() {
		try {
			data = objectMapper.readValue(FILE, MVConfiguration.class);
			return true;
		} catch (Exception e) {
			e.printStackTrace();
			return false;
		}
	}
	//=============================================================================================

	//=============================================================================================
	public static boolean store() {
		try {
			objectMapper.writeValue(FILE, data);
			return true;
		} catch (Exception e) {
			e.printStackTrace();
			return false;
		}
	}
	//=============================================================================================

	//=============================================================================================
	public MVWifi     wifi;
	public MVRotation rotation;
	public MVPivots   pivots;
	public MVCoords   headPosition;
	public Float      headRadius;
	//=============================================================================================
	
}
//*************************************************************************************************
