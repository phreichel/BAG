//**************************************************************************************************
package joda.breakout.model;
//**************************************************************************************************

import javax.vecmath.Color3f;

import lombok.Getter;

//**************************************************************************************************
public enum Block {

	//==============================================================================================
	NORMAL(1, Colors.LIGHT_BLUE50, Colors.BLACK),	
	CONCRETE(2, Colors.GREY50, Colors.BLACK),	
	STEEL(5, Colors.DARK_BLUE60, Colors.WHITE),	
	OBSIDIAN(10, Colors.BLACK, Colors.DARK_MAGENTA40),
	WALL(10, Colors.DARK_CYAN40, Colors.DARK_CYAN40);
	//==============================================================================================
	
	//==============================================================================================
	@Getter private int hitCount;
	@Getter private Color3f blockColor;
	@Getter private Color3f edgeColor;
	//==============================================================================================
	
	//==============================================================================================
	private Block(
			int hitCount,
			Color3f blockColor,
			Color3f edgeColor) {
		this.hitCount = hitCount;
		this.blockColor = blockColor;
		this.edgeColor = edgeColor;
	}
	//==============================================================================================
	
}
