//*****************************************************************************
package gameengine.app;
//*****************************************************************************

//*****************************************************************************
public abstract class Application {

	//=========================================================================
	private boolean terminated = false;
	//=========================================================================
	
	//=========================================================================
	public void setup(String ... args) {};
	//=========================================================================

	//=========================================================================
	public void terminate() {
		terminated = true;
	}
	//=========================================================================
	
	//=========================================================================
	public void run() {
		init();
		loop();
		done();
	}
	//=========================================================================

	//=========================================================================
	protected void init() {};
	//=========================================================================

	//=========================================================================
	protected void update() {};
	//=========================================================================

	//=========================================================================
	protected void loop() {
		while (!terminated) {
			update();
			try {
				Thread.sleep(1L);
			} catch (InterruptedException e) {
				e.printStackTrace();
			}
		}
	};
	//=========================================================================

	//=========================================================================
	protected void done() {};
	//=========================================================================
	
}
//*****************************************************************************
