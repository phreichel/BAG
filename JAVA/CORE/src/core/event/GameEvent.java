package core.event;
//************************************************************************************************

//************************************************************************************************
public class GameEvent {

	//============================================================================================
	public Enum<?> type  = null;
	public String  text  = null;
	public float   value = Float.NaN;
	public Object  data  = null;
	//============================================================================================

	//============================================================================================
	public void clear() {
		type  = null;
		text  = null;
		value = Float.NaN;
		data  = null;
	}
	//============================================================================================
	
	//============================================================================================
	@SuppressWarnings("unchecked")
	public <T> T data(Class<T> c) {
		return (T) data;
	};
	//============================================================================================
	
}
