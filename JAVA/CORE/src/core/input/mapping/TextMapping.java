//************************************************************************************************
package core.input.mapping;
//************************************************************************************************

import core.input.raw.InputEvent;

//************************************************************************************************
public class TextMapping implements IInputMapping {

	//============================================================================================
	protected IInputAction onText = null;
	//============================================================================================

	//============================================================================================
	public TextMapping() {
		this(null);
	}
	//============================================================================================
	
	//============================================================================================
	public TextMapping(IInputAction onText) {
		this.onText = onText;
	}
	//============================================================================================

	//============================================================================================
	@Override
	public void update(InputEvent event, InputState state) {
		if (
			onText != null &&
			event.character != '\0' &&
			event.value == InputEvent.VALUE_TYPED
		) {
			onText.perform(event, state);
		}
	}
	//============================================================================================
	
}
//************************************************************************************************
