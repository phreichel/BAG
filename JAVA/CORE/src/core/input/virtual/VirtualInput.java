//************************************************************************************************
package core.input.virtual;
//************************************************************************************************

import java.util.ArrayList;
import java.util.List;

//************************************************************************************************
public class VirtualInput {

	//============================================================================================
	private List<IVirtualAction> actionList = new ArrayList<>();
	private List<IVirtualAxis>   axisList = new ArrayList<>();
	private List<IVirtualText>   textList = new ArrayList<>();
	//============================================================================================

	//============================================================================================
	public void addAction(IVirtualAction action) {
		if (!actionList.contains(action)) {
			actionList.add(action);
		}
	}
	//============================================================================================

	//============================================================================================
	public boolean removeAction(IVirtualAction action) {
		return actionList.remove(action);
	}
	//============================================================================================
	
	//============================================================================================
	public void addAxis(IVirtualAxis axis) {
		if (!axisList.contains(axis)) {
			axisList.add(axis);
		}
	}
	//============================================================================================

	//============================================================================================
	public boolean removeAxis(IVirtualAxis axis) {
		return axisList.remove(axis);
	}
	//============================================================================================
	
	//============================================================================================
	public void addText(IVirtualText text) {
		if (!textList.contains(text)) {
			textList.add(text);
		}
	}
	//============================================================================================

	//============================================================================================
	public boolean removeText(IVirtualText text) {
		return textList.remove(text);
	}
	//============================================================================================
	
	//============================================================================================
	public void update(int nFrames, long periodNs) {
		
		for (var action : actionList) {
			action.update(nFrames, periodNs);
		}

		for (var axis : axisList) {
			axis.update(nFrames, periodNs);
		}

		for (var text : textList) {
			text.update(nFrames, periodNs);
		}
		
	}
	//============================================================================================
	
}
//************************************************************************************************
