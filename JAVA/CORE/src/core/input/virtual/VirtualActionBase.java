//************************************************************************************************
package core.input.virtual;
//************************************************************************************************

//************************************************************************************************
public abstract class VirtualActionBase implements IVirtualAction {

	//============================================================================================
	private final String  ident;
	private final String  action;
	protected     boolean hasTriggered = false;
	//============================================================================================

	//============================================================================================
	public VirtualActionBase(String ident, String action) {
		this.ident  = ident;
		this.action = action;
	}
	//============================================================================================
	
	//============================================================================================
	@Override
	public String getIdent() {
		return this.ident;
	}
	//============================================================================================

	//============================================================================================
	@Override
	public String getAction() {
		return this.action;
	}
	//============================================================================================
	
	//============================================================================================
	@Override
	public boolean hasTriggered() {
		return this.hasTriggered;
	}
	//============================================================================================

	//============================================================================================
	@Override
	public void confirmTriggered() {
		this.hasTriggered = false;
	}
	//============================================================================================

}
//************************************************************************************************
