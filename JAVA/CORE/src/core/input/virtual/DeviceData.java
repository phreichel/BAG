//************************************************************************************************
package core.input.virtual;
//************************************************************************************************

import java.util.Collections;
import java.util.HashMap;
import java.util.HashSet;
import java.util.Map;
import java.util.Set;

//************************************************************************************************
public class DeviceData {

	//============================================================================================
	private Map<String,Float> axes    = new HashMap<>();
	private Set<String>       actions = new HashSet<>();
	private Map<String,Float> axesReadonly = Collections.unmodifiableMap(axes);
	private Set<String>       actionsReadonly = Collections.unmodifiableSet(actions);
	//============================================================================================

	//============================================================================================
	public void set(String action) {
		actions.add(action);
	}
	//============================================================================================
	
	//============================================================================================
	public void set(String axis, Float value) {
		axes.put(axis, value);
	}
	//============================================================================================

	//============================================================================================
	public Set<String> getActions() {
		return actionsReadonly;
	}
	//============================================================================================
	
	//============================================================================================
	public Map<String,Float> getAxes() {
		return axesReadonly;
	}
	//============================================================================================
	
	//============================================================================================
	public void clear() {
		axes.clear();
		actions.clear();
	}
	//============================================================================================
	
}
//************************************************************************************************
