package core.clock;
//************************************************************************************************

//************************************************************************************************
class Schedule {

	//============================================================================================#
	public long offset = 0L;
	public long period = 0L;
	public long accum  = 0L;
	public ITask task = null;
	//============================================================================================#

	//============================================================================================#
	public Schedule(long period, ITask task) {
		this.period = period;
		this.task = task;
	}
	//============================================================================================#
	
	//============================================================================================#
	public void update(long delta) {
		accum += delta;
		int frames = (int) (accum / period);
		if (frames > 0) {
			if (task != null) task.update(frames, period);
		}
	}
	//============================================================================================#
	
}
//************************************************************************************************
