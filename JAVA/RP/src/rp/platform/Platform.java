//*************************************************************************************************
package rp.platform;
//*************************************************************************************************

import com.jogamp.newt.opengl.GLWindow;
import com.jogamp.opengl.GLCapabilities;
import com.jogamp.opengl.GLProfile;

//*************************************************************************************************
public class Platform {

	//=============================================================================================
	private static final String WINDOW_DEFAULT_TITLE = "RP Default Window";
	private static final int WINDOW_DEFAULT_WIDTH = 800;
	private static final int WINDOW_DEFAULT_HEIGHT = 600;
	//=============================================================================================
	
	//=============================================================================================
	private final GLWindow window;
	//=============================================================================================
	
	//=============================================================================================
	public Platform() {
		var glProfile = GLProfile.getDefault();
		var glCaps = new GLCapabilities(glProfile);
		window = GLWindow.create(glCaps);
	}
	//=============================================================================================

	//=============================================================================================
	public void init() {
		window.setTitle(WINDOW_DEFAULT_TITLE);
		window.setSize(WINDOW_DEFAULT_WIDTH, WINDOW_DEFAULT_HEIGHT);
		// window.setMaximized(true, true);
		window.setVisible(true);
	}
	//=============================================================================================

	//=============================================================================================
	public void update() {
		window.display();
	}
	//=============================================================================================
	
	//=============================================================================================
	public void done() {
		window.setVisible(false);
	}
	//=============================================================================================
	
}
//*************************************************************************************************
