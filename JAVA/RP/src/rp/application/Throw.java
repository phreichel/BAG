//*************************************************************************************************
package rp.application;
//*************************************************************************************************

//*************************************************************************************************
public class Throw extends RuntimeException {

	//=============================================================================================
	private static final long serialVersionUID = 1L;
	//=============================================================================================
	
	//=============================================================================================
	public Throw() {
		super();
	}
	//=============================================================================================

	//=============================================================================================
	public Throw(String message) {
		super(message);
	}
	//=============================================================================================

	//=============================================================================================
	public Throw(Throwable cause) {
		super(cause);
	}
	//=============================================================================================

	//=============================================================================================
	public Throw(String message, Throwable cause) {
		super(message, cause);
	}
	//=============================================================================================

	//=============================================================================================
	public Throw(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
	//=============================================================================================

}
//*************************************************************************************************
