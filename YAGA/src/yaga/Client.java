//*****************************************************************************
package yaga;
//*****************************************************************************

import com.jogamp.newt.opengl.GLWindow;
import com.jogamp.opengl.GLCapabilities;
import com.jogamp.opengl.GLProfile;

//*****************************************************************************
public class Client {

	//=========================================================================
	private GLWindow window;
	//=========================================================================
	
	//=========================================================================
	private Audio audio;
	private Video video;
	private Input input;
	private UI    ui;
	//=========================================================================

	//=========================================================================
	public Client() {
		var profile = GLProfile.getDefault();
		var caps = new GLCapabilities(profile);
		window = GLWindow.create(caps);
		window.setFullscreen(true);
		audio = new Audio();
		video = new Video(window);
		input = new Input(window);
		ui = new UI();
	}
	//=========================================================================
	
	//=========================================================================
	public void init() {
		audio.init();
		video.init();
		input.init();
		video.load(ui);
	}
	//=========================================================================

	//=========================================================================
	public void update(float dT) {
		ui.update(dT);
		audio.update();
		video.update();
		input.update();
	}
	//=========================================================================

	//=========================================================================
	public void done() {
		video.unload(ui);
		audio.done();
		video.done();
		input.done();
	}
	//=========================================================================
	
}
//*****************************************************************************
