#include "Cable.h"
#include "Hardware.h"

//=============================================================================
Hardware hardware;
Console  console(&hardware);
//=============================================================================

//=============================================================================
void cableSetup() {
	console.setup();
	hardware.setup();
	hardware.enabled(true);
}
//=============================================================================


//=============================================================================
void cableLoop() {
	console.loop();
	hardware.loop();
}
