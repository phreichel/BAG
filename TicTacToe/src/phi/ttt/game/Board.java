package phi.ttt.game;
//*********************************************************************************************************************

//*********************************************************************************************************************
public class Board {

	//=================================================================================================================
	private Stone[][] table = new Stone[3][3];
	//=================================================================================================================

	//=================================================================================================================
	public void set(Move move) {
		var row   = move.row();
		var col   = move.column();
		var stone = Stone.BLUE; 
		table[row][col] = stone;
	}
	//=================================================================================================================

	//=================================================================================================================
	public void unset(Move move) {
		var row   = move.row();
		var col   = move.column();
		table[row][col] = null;
	}
	//=================================================================================================================
	
}
